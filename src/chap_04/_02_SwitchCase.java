package chap_04;

public class _02_SwitchCase {
    public static void main(String[] args) {

        int rank = 4;

        switch (rank){
            case 1:
                System.out.println("www");
                break;
            case 2:
                System.out.println("ddd");
                break;
            case 4:
                System.out.println("bbbb");
                break;
            default:
                System.out.println("mmm");
        }
    }
}
